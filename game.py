    def next_turn(self):
        time.sleep(1)
        os.system('cls' if os.name == 'nt' else 'clear')

        self.current = self.current + self.addition
        print("Your score: {0: .3f}".format(self.current))
        print("Your income: {0: .3f} point per second".format(self.addition))
        print("Upgrade will give you additional {0:.3f} points per second".format(self.upgraded_addition))
        print("Upgrade cost: {1: .3f} points".format(self.upgraded_addition, self.upgrade_cost))
        print(self.castles[self.upgrades if self.upgrades < len(self.castles) else -1])
        if self.current > self.upgrade_cost:
            self.upgrade()

    def upgrade(self):
        print("UPGRADED!")
        self.addition = self.addition + self.upgraded_addition
        self.current = self.current - self.upgrade_cost
        self.upgrades = self.upgrades + 1
        self.upgrade_cost = self.upgrade_cost * 1.17 ** self.upgrades
        self.addition = self.upgraded_addition
        self.upgraded_addition = self.upgraded_addition + self.upgraded_addition * 1.17


